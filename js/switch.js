// switch statement
let day = 8;
switch (day) {
  case 1:
    console.log("monday");
    break;
  case 2:
    console.log("tuesday");
    break;
  case 3:
    console.log("wed");
    break;
  case 4:
    console.log("thursday");
    break;
  case 5:
    console.log("friday");
    break;
  case 6:
    console.log("sat");
    break;
  case 7:
    console.log("sunday");
    break;
  default:
    console.log("invalid value");
}

